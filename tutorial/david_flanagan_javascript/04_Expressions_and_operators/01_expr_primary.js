/* Cap.4 Expressions and Operators
   4.1 Primary Expressions

   $ node 01_expr_primary.js

   Primary expressions are:
   - the simpler expressions
   - constant or literal values
   - certain language keywords
   - variable references
*/


1.23              // A mumber literal
"hello"           // A string literal
;
/pattern/         // A regular expression literal

true              // Evaluates to the boolean true value
false             // Evaluates to the boolean false value
null              // Evaluates to the null value (keyword)
this              // Evaluates to the "current" object (constant)

var i, sum;
i;                // Evaluates to the value of variable i
sum;              // Evaluates to the valud of the variable sum
undefined;        // undefined is a global variable, not a keywork like null.
