/* Cap.3 Types, Values and Variables
   3.2.1 Floating-Point Literals

   JavaScript uses the traditional syntax for real numbers.
   [digits][.digits][(E|e)[(+|-)]digits]

   Examples:
*/

var n1 = 3.14                 // output: 3.14
var n2 = 2345.789             // output: 2345.789
var n3 = .333333333333333333  // output: 0.3333333333333333
var n4 = 6.02e23              // output: 6.02e+23
var n5 = 1.4738223E-32        // output: 1.4738223e-32
