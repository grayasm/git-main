/* Cap.2 Lexical Structure
   2.4   Identifiers and Reserved Words

   An identifier is simply a name. In JavaScript, identifiers are used to name
   variables and functions and to provide labels for centain loops in JavaScript
   code. It must begin with a letter, underscore (_) or dollar sign ($)
   followed by letters, digits, underscores or dollar signs.
*/


var i                = 1
var my_variable_name = 2
var v13              = 3
var _dummy           = 4
var $str             = "str"


/*  JavaScript allows identifiers to contain letters and digits from the entire
    Unicode character set.
*/
var sí = true;
var π = 3.14;
