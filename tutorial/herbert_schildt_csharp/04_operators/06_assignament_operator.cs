using System;

class Example
{
    static void Main()
    {
        // The Assignment Operator
        int x, y, z;
        x = y = z = 100; // set x, y, and z to 100

        // Compound Assignments
        // Thus, the arithmetic and logical assignment operators are:
        x += 10;
        x -= 10;
        x *= 10;
        x /= 10;
        x %= 10;
        x &= 10;
        x |= 10;
        x ^= 10;

    }
}