# https://docs.python.org/3.3/library/math.html
import math


# Number-theoretic and representation functions
print('          ceil(3.1)=%f' % math.ceil(3.1))            # ret: 4
print('copysign(4.3, -3.2)=%f' % math.copysign(4.3, -3.2))  # ret: -4.3
print('       fabs(-3.399)=%f' % math.fabs(-3.399))         # ret: 3.399

