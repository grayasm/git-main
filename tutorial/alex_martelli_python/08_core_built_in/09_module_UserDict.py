#           The main content of the UserDict module of Python’s standard
#           library (analogous to those of now semi-obsolete modules UserList
#           and UserString ) used to be classes that emulated the behavior of
#           standard built-in container types. Their usefulness used to be
#           that, in the legacy object model, you could not inherit from
#           built-in types, and what we now call the legacy object model used
#           to be the only object model available in Python. Nowadays, you can
#           simply subclass built-in types such as list and dict , so there
#           isn’t much point in emulating the built-in types by Python-coded
#           classes.
