float tabla_tragere::tabla_tragere_cautare(int nr_bat, int coloana_referinta, float valoarea_referinta, int coloana_dorita)
{
    int lsup;
    int linf;

    float interval;
    float variatia;
    float rezultat = 0;

    int gasit = 0 ;

    for(int i=0; i < tabla_tragere_plafon ; ++i)
    {
        if(valoarea_referinta <= tabla_tragere_v5[i][coloana_referinta] )
        {
            gasit = 1 ;
            lsup = i;
            linf = i-1 ;
            interval = tabla_tragere_v5[lsup][coloana_referinta] - tabla_tragere_v5[linf][coloana_referinta] ;
            variatia = valoarea_referinta - tabla_tragere_v5[linf][coloana_referinta] ;

            rezultat =  Util::interpoleaza(
                variatia, 
                tabla_tragere_v5[linf][coloana_dorita],
                tabla_tragere_v5[lsup][coloana_dorita], 
                interval);

            i=tabla_tragere_plafon+100 ; 
            continue ;
        }
    }


    if(gasit == 0)
    {
        printf("\n\n");
        printf("\n               Eroare in tabla de tragere");
        printf("\n      Bateria %d Artilerie", nr_bat+1);
        printf("\n      Distanta de intr. in tabla = %.2f",  valoarea_referinta);
        printf("\n      tabla_tragere_incarcatura[%d] = %.0f",nr_bat, tabla_tragere_incarcatura[nr_bat]);
        printf("\n      distanta inf = %.2f", tabla_tragere_v5[0][coloana_referinta]);
        printf("\n      distanta sup = %.2f", tabla_tragere_v5[tabla_tragere_plafon-1][coloana_referinta]);
        getchar();

        g_control_lucru.control_lucru_intrerupe_calcul_depasire_limite[nr_bat] = 1 ;

    }

    return rezultat;
};
